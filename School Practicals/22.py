# Write a program of basic Math Module
# Importing math module
import math
# The square root function
print("Printing square root of 216")
print(math.sqrt(216))
# The ceil floor functions
print("The ceil integer of 5.3453")
print(math.ceil(5.3453))
print("The floor integer of 5.3453")
print(math.floor(5.3453))
# The power function
print("2 raised to 10")
print(math.pow(2, 10))
# Absolute value function
print("The absolute value of -2.32")
print(math.fabs(-2.32))
# Trigonometry
print("The sin, cos and tan functions of angle 30 degrees or pi by 6 radians")
angle = math.pi / 6
print(math.sin(angle))
print(math.cos(angle))
print(math.tan(angle))